def _sub_field_result(field, sub_field):
    """根据二级分类获取汽车金融映射结果，未知分类则使用通用兜底。"""
    if sub_field in AUTO_ANALYSIS_RULES:
        return dict(AUTO_ANALYSIS_RULES[sub_field])
    if field == "信贷业务":
        return dict(AUTO_ANALYSIS_RULES["贷款发放与用途管理"])
    return _generic_auto_result()


def auto_analysis(violation, primary_field, primary_sub_field, all_hits):
    """
    基于所有命中的二级分类综合判断汽车金融映射优先级。
    优先选择映射风险等级最高的结果；同等级下优先保留主分类的结果，
    并合并同等级命中的标签与建议。
    """
    if not all_hits:
        return _generic_auto_result()

    priority = {"高": 3, "中": 2, "低": 1}
    results = []
    for h in all_hits:
        r = _sub_field_result(h["field"], h["sub_field"])
        r["_field"] = h["field"]
        r["_sub_field"] = h["sub_field"]
        results.append(r)

    # 按风险等级降序
    results.sort(key=lambda r: priority.get(r["level"], 0), reverse=True)
    top_level = results[0]["level"]
    top_results = [r for r in results if r["level"] == top_level]

    # 同等级下优先保留主分类，保证主分类稳定性
    selected = top_results[0]
    for r in top_results:
        if r["_sub_field"] == primary_sub_field:
            selected = r
            break

    # 合并同等级命中的标签与建议（去重并保持顺序）
    tags = []
    advice = []
    for r in top_results:
        tags.extend(r["tags"])
        advice.extend(r["advice"])
    tags = list(dict.fromkeys(tags))
    advice = list(dict.fromkeys(advice))

    mapping = selected["mapping"]
    if len(top_results) > 1:
        mapping = (
            f"该处罚同时命中多个汽车金融相关分类，综合判定映射优先级为{top_level}。"
            f"核心映射：{mapping}"
        )

    return {
        "mapping": mapping,
        "level": top_level,
        "tags": tags,
        "advice": advice,
    }


