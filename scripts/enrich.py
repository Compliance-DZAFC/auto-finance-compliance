def main():
    if not os.path.exists(RAW_FILE):
        print(f"[ERROR] 找不到原始数据文件: {RAW_FILE}")
        print("请先运行: python scripts/scrape.py")
        return

    with open(RAW_FILE, "r", encoding="utf-8") as f:
        raw_data = json.load(f)

    print(f"[INFO] 加载原始记录: {len(raw_data)} 条")

    existing = load_existing_enriched()
    print(f"[INFO] 已有 enriched 记录: {len(existing)} 条")

    enriched_records = []
    llm_count = 0
    review_count = 0
    reused = 0
    multi_tag_count = 0

    for r in raw_data:
        key = record_key(r)
        if key in existing and not existing[key].get("review_needed") and "sub_field" in existing[key]:
            enriched_records.append(existing[key])
            reused += 1
            if len(existing[key].get("category_tags", [])) > 0:
                multi_tag_count += 1
            continue

        e = enrich_record(r, existing)
        enriched_records.append(e)
        if e.get("llm_used"):
            llm_count += 1
        if e.get("review_needed"):
            review_count += 1
        if len(e.get("category_tags", [])) > 0:
            multi_tag_count += 1

    save_enriched(enriched_records)

    print(f"[DONE] 已保存 enriched 数据: {ENRICHED_FILE}")
    print(f"  - 总记录: {len(enriched_records)} 条")
    print(f"  - 复用缓存: {reused} 条")
    print(f"  - 多标签记录: {multi_tag_count} 条")
    print(f"  - LLM 兜底: {llm_count} 条")
    print(f"  - 待人工复核: {review_count} 条")
    print()
    print("使用说明:")
    print("  1. 直接运行 build.py 时，改读 penalties_enriched.json 即可跳过分类/分析")
    print("  2. 如需启用 LLM 兜底: 设置环境变量 KIMI_API_KEY=sk-xxx 后重新运行 enrich.py")
    print("  3. 人工复核: 打开 data/penalties_enriched.json，搜索 review_needed=true 的记录，")
    print("     修改 field/sub_field 后删除 review_needed 标记，重新运行 enrich.py 会复用")


if __name__ == "__main__":
    main()
