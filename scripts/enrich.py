def parse_amount(text):
    if not text:
        return 0
    matches = re.findall(r"罚款\s*(\d+(?:\.\d+)?)\s*万", text)
    if matches:
        return sum(float(m) for m in matches)
    matches2 = re.findall(r"(\d+(?:\.\d+)?)\s*万元", text)
    if matches2:
        return max(float(m) for m in matches2)
    return 0


