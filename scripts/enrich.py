# LLM 缓存：同一个 violation 文本只调用一次 LLM，避免同案多记录分类不一致
_llm_cache = {}

def enrich_record(raw, existing=None):
    """
    对单条原始记录进行增强处理。
    如果 existing 存在且不需要重新处理，直接返回 existing。
    """
    key = record_key(raw)

    # 已有 enriched 记录，且不是人工标记待复核的，并且包含新字段，直接复用
    if existing and key in existing:
        old = existing[key]
        if not old.get("review_needed") and "sub_field" in old:
            return old

    # 基础解析
    amount = parse_amount(raw.get("penalty", ""))
    inst_type = classify_institution_type(raw.get("party", ""))
    penalty_type = classify_penalty_type(raw.get("penalty", ""))

    # 二级分类命中（多选）
    violation = raw.get("violation", "")
    sub_hits = classify_sub_fields(violation)
    primary, confidence = pick_primary(sub_hits)
    field = primary["field"] if primary else "其他"
    sub_field = primary["sub_field"] if primary else "其他"

    review_needed = False
    llm_used = False

    # 低置信度 -> LLM 兜底（如果配置了 API Key）
    # 只有 confidence=low 才调用 LLM；medium 和 high 信任规则结果
    if confidence == "low" and LLM_API_KEY:
        # 同 violation 缓存：避免同一个处罚案由的多条记录得到不同 LLM 分类结果
        cache_key = violation.strip()
        if cache_key in _llm_cache:
            field, sub_field = _llm_cache[cache_key]
            llm_used = True
            confidence = "medium"
        else:
            llm_result = llm_classify(violation, candidates=sub_hits)
            if llm_result:
                field, sub_field = llm_result
                _llm_cache[cache_key] = (field, sub_field)
                llm_used = True
                # LLM 结果默认 confidence 提升为 medium
                confidence = "medium"
    elif confidence == "low" and not LLM_API_KEY:
        review_needed = True

    # 汽车金融映射分析：基于所有命中分类综合判断
    analysis = auto_analysis(violation, field, sub_field, sub_hits)

    # 其余命中二级分类放入 category_tags
    category_tags = [h["sub_field"] for h in sub_hits if h["sub_field"] != sub_field]

    # 一级领域命中聚合（兼容旧字段）
    field_hits = aggregate_field_hits(sub_hits)

    enriched = {
        # 原始字段保留
        "doc_id": raw.get("doc_id", ""),
        "doc_no": raw.get("doc_no", ""),
        "seq": raw.get("seq", ""),
        "party": raw.get("party", ""),
        "violation": violation,
        "penalty": raw.get("penalty", ""),
        "authority": raw.get("authority", ""),
        "source_url": raw.get("source_url", ""),
        "publish_date": raw.get("publish_date", ""),
        "rowspan_group": raw.get("rowspan_group", 0),

        # 增强字段
        "amount": amount,
        "institution_type": inst_type,
        "penalty_type": penalty_type,
        "field": field,
        "sub_field": sub_field,
        "field_confidence": confidence,
        "category_tags": category_tags,
        "field_hits": field_hits,
        **analysis,
        "review_needed": review_needed,
        "llm_used": llm_used,
        "enriched_at": datetime.now().isoformat(),
    }
    return enriched


