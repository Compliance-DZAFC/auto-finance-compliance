def classify_sub_fields(violation):
    """
    返回按命中关键词数降序排列的二级分类命中列表。
    每个元素：{"field": 一级领域, "sub_field": 二级分类, "score": 命中数, "keywords": [命中词]}
    
    匹配策略：
    1. 检查显式 keywords 是否在文本中
    2. 检查 sub_field 的末尾4字核心词是否在文本中（自动派生，解决"非房地产项目融资"匹配"项目融资"等前缀修饰问题）
    3. 检查 sub_field 本身是否在文本中
    """
    if not violation:
        return []

    v = violation.lower()
    hits = []
    for field, sub_field, keys in SUB_FIELD_KEYWORDS:
        matched = [k for k in keys if k in v]
        
        # 自动派生：sub_field 末尾4字核心词（如"非房地产项目融资"→"项目融资"）
        if len(sub_field) >= 4:
            tail = sub_field[-4:]
            if tail in v and tail not in matched:
                matched.append(tail)
        
        # sub_field 本身也在 keywords 中（之前批量修复已加入），
        # 但如果 keywords 列表中没有 sub_field，这里兜底检查
        if sub_field in v and sub_field not in matched:
            matched.append(sub_field)
        
        if matched:
            hits.append({
                "field": field,
                "sub_field": sub_field,
                "score": len(matched),
                "keywords": matched,
            })

    hits.sort(key=lambda x: x["score"], reverse=True)
    return hits


def pick_primary(hits):
    """
    从命中列表中选出置信度最高的一级领域 + 二级分类。
    返回 (primary_hit, confidence)。
    confidence: high | medium | low
    """
    if not hits:
        return None, "low"

    top_score = hits[0]["score"]
    ties = [h for h in hits if h["score"] == top_score]

    if len(hits) == 1:
        confidence = "high"
    elif len(ties) == 1:
        confidence = "medium"
    else:
        confidence = "low"

    return hits[0], confidence


def aggregate_field_hits(sub_hits):
    """
    将二级分类命中聚合为一级领域命中（兼容旧版 field_hits 结构）。
    返回 [[field, score, [keywords]], ...]，按 score 降序。
    """
    groups = {}
    for h in sub_hits:
        f = h["field"]
        if f not in groups:
            groups[f] = {"score": 0, "keywords": set()}
        groups[f]["score"] += h["score"]
        groups[f]["keywords"].update(h["keywords"])

    return sorted(
        [[f, g["score"], sorted(g["keywords"])] for f, g in groups.items()],
        key=lambda x: x[1],
        reverse=True,
    )


# ─── LLM 兜底分类 ───
def llm_classify(violation, candidates=None):
    """
    调用 LLM 对违规事由进行精确分类。
    返回 (field, sub_field) 或 None（未配置 API Key 或调用失败）。
    """
    if not LLM_API_KEY:
        return None

    try:
        import openai
        client = openai.OpenAI(api_key=LLM_API_KEY, base_url=LLM_API_BASE)

        valid_fields = sorted({h[0] for h in SUB_FIELD_KEYWORDS})
        valid_sub_fields = sorted({f"{h[0]}/{h[1]}" for h in SUB_FIELD_KEYWORDS})

        candidate_text = ""
        if candidates:
            candidate_text = "此前规则命中了以下二级分类（按命中关键词数排序）：" + "、".join(
                f"{c['field']}/{c['sub_field']}({c['score']}个关键词)" for c in candidates
            ) + "；请综合判断最主要的一个。"

        prompt = f"""你是一位银行监管合规专家。请根据以下行政处罚的违规事由，判断其最主要属于哪个业务领域及二级分类。

可选一级领域：{"、".join(valid_fields)}
可选二级分类（格式：field/sub_field）：
{"、".join(valid_sub_fields)}

{candidate_text}
违规事由：{violation}

请只输出一个二级分类，格式：field/sub_field。不要解释。如果涉及多个分类，输出最主要的一个。"""

        resp = client.chat.completions.create(
            model=LLM_MODEL,
            messages=[{"role": "user", "content": prompt}],
            temperature=1,
            max_tokens=30,
        )
        result = resp.choices[0].message.content.strip()

        # 优先匹配完整的 field/sub_field
        for field, sub_field, _ in SUB_FIELD_KEYWORDS:
            if f"{field}/{sub_field}" in result:
                return field, sub_field

        # 退一步匹配 sub_field 或 field
        for field, sub_field, _ in SUB_FIELD_KEYWORDS:
            if sub_field in result:
                return field, sub_field
        for field, sub_field, _ in SUB_FIELD_KEYWORDS:
            if field in result:
                return field, sub_field

        return "其他", "其他"
    except Exception as e:
        print(f"[WARN] LLM 调用失败: {e}")
        return None


