def classify_penalty_type(penalty):
    if not penalty:
        return "其他"
    p = penalty.lower()
    types = []
    if "罚款" in p or "罚没" in p or "没收" in p:
        types.append("罚款/没收")
    if "警告" in p:
        types.append("警告")
    if "禁止" in p or "取消" in p or "撤销" in p:
        types.append("禁止/取消资格")
    if "责令改正" in p or "整改" in p:
        types.append("责令改正")
    if not types:
        return "其他"
    return "、".join(types)


